    public void rsp1(String pal) {
        txt_det_bus.append("------------------------------------------------" + "\n");
        txt_det_bus.append("RESULTADO DE BUSQUEDA POR PLANTA\n");

        String queryString = "PREFIX rdf: <http://www.w3.org/1999/02/22-rdf-syntax-ns#> "
                + "PREFIX dbo: <http://dbpedia.org/ontology/> "
                + "PREFIX schema: <http://www.w3.org/2000/01/rdf-schema#> "
                + "PREFIX dct: <http://purl.org/dc/terms/> "
                + "PREFIX skos: <http://www.w3.org/2004/02/skos/core#> "
                + "PREFIX dbo: <http://dbpedia.org/ontology/> "
                + "PREFIX wo: <http://purl.org/ontology/wo/> "
                + "SELECT DISTINCT ?nombre ?g ?f ?label ?abs "
                + "WHERE {"
                + "?Planta rdf:type dbo:Plant ; schema:label ?label ; dbo:name ?nombre ; dbo:Abstract ?abs ; wo:genus ?g ; wo:family ?f ."
                + "FILTER regex(?nombre , '" + pal + "','i')}limit 1";
        pregunta2(queryString);
    }
