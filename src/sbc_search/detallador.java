    public void pregunta2(String queryString) {
        //QueryExecution qexec1 = QueryExecutionFactory.sparqlService(service, queryString, defaultGraph);
        QueryExecution qexec1 = QueryExecutionFactory.sparqlService(service, queryString);
        ResultSet results = qexec1.execSelect();
        while (results.hasNext()) {
            QuerySolution sol = results.nextSolution();
            RDFNode nombre = sol.get("nombre");
            RDFNode abs = sol.get("g");
            RDFNode label = sol.get("f");
            RDFNode cat = sol.get("label");
            RDFNode plan = sol.get("abs");

            txt_det_bus.append("------------------------------------------------" + "\n");
            txt_det_bus.append("Nombre de Planta: " + "\n" + nombre.toString() + "\n");
            txt_det_bus.append("------------------------------------------------" + "\n");
            txt_det_bus.append("Genus : " + "\n" + abs.toString() + "\n");
            txt_det_bus.append("------------------------------------------------" + "\n");
            txt_det_bus.append("Family: " + "\n" + label.toString() + "\n");
            txt_det_bus.append("------------------------------------------------" + "\n");
            txt_det_bus.append("Nombre Cientifico : " + "\n" + cat.toString() + "\n");
            txt_det_bus.append("------------------------------------------------" + "\n");
            txt_det_bus.append("Abstracto : " + "\n" + plan.toString() + "\n");
            txt_det_bus.append("------------------------------------------------" + "\n");
        }
        //System.out.close();

    }

